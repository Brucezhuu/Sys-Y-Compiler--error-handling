public class PCode_Func {
    private int index;
    private int paraNum;

    public PCode_Func(int index, int paraNum) {
        this.index = index;
        this.paraNum = paraNum;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public int getParaNum() {
        return paraNum;
    }

    public void setParaNum(int paraNum) {
        this.paraNum = paraNum;
    }
}
